# ! Escape Sequence Examples

# 1. New Line -> \n
print("Hello\n\nWorld")

# 2. Horizontal Tab -> \t
print("Hello\t\tWorld")

# 3. Backslash -> \\
print("Hello\\World")

# 4. Double Quote -> \"
print("Hello \"World\"")

# 5. Single Quote -> \'
print("Hello \'World\'")

# 6. BackSpace -> \b
print("Hello\b\b\bWorld")

# 7. Carriage Return -> \r
print("Hello\rWorld")
print("abca\rb")
print("Loading.\rLoading..\rLoading...")
print("com\rcomplete")
